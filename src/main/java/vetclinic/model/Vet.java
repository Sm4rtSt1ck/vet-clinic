package vetclinic.model;

public class Vet {
    private int vetId;
    private String name;
    private String officeNumber;
    private String phoneNumber;

    public int getVetId() {
        return vetId;
    }
    public String getName() {
        return name;
    }
    public String getOfficeNumber() {
        return officeNumber;
    }
    public String getPhoneNumber() {
        return phoneNumber;
    }
}
