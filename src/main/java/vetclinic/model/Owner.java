package vetclinic.model;

public class Owner {
    private int ownerId;
    private String name;
    private String address;
    private String phoneNumber;

    public int getOwnerId() {
        return ownerId;
    }

    public String getName() {
        return name;
    }

    public String getAddress() {
        return address;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }
}
